class FoodItems{
	public static double price(String foodName){
		if (foodName=="Rice"){		
			return 40;
		}
		else if (foodName=="Daal"){		
			return 50;
		}
		else if (foodName=="Sambhar"){		
			return 20;
		}
		else if (foodName=="Samosa"){		
			return 25;
		}
		else if (foodName=="Food"){		
			return 100;
		}
		else if (foodName=="Biriyani"){		
			return 240;
		}
		else if (foodName=="Pulav"){		
			return 60;
		}
		else if (foodName=="Fruit"){		
			return 90;
		}
		else if (foodName=="Noodles"){		
			return 110;
		}
		else if (foodName=="Something"){		
			return 70;
		}
		else if (foodName=="Chapati"){		
			return 30;
		}
		else if (foodName=="Paneer"){		
			return 60;
		}
		else if (foodName=="ChatMasala"){		
			return 80;
		}
		else if (foodName=="GobiManchuri"){		
			return 60;
		}
		return 0;
	}
	
	
}